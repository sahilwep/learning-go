/*
 - Go has very value type including strings, integers, floats, booleans, etc.
 - Here are a few basics example:


*/

package main

import "fmt"

func main() {

	fmt.Println("go" + "lang")

	fmt.Println("1 + 1 = ", 1+1)
	fmt.Println("7.0/3.0 = ", 7.0/3.0)

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
